def convert_to_usd(amount_rub):
    rate = 78.46  # 1 USD = 78.46 RUB
    return amount_rub / rate

def convert_to_eur(amount_rub):
    rate = 91.40  # 1 EUR = 91.40 RUB
    return amount_rub / rate

def convert_to_gbp(amount_rub):
    rate = 106.16  # 1 GBP = 106.16 RUB
    return amount_rub / rate